
public class BasicMath {

	public static void main(String[] args) {
		// Finding the average of three numbers
		float num1 = 3.1f;
		float num2 = 6.23f;
		float num3 = 14.7f;
		
		System.out.println("The average of these three numbers is " + (num1 + num2 + num3) / 3 + ".");
		
		// Find the area of a rectangle
		float height = 18.3f;
		float width = 6.6f;
		
		double rectArea = height * width;
		
		System.out.println("The area of the rectangle is " + rectArea + ".");
	}

}
